package com.github.ushiosan23.android_networkutils.exceptions;

public class UnInitializePropertyException extends RuntimeException {

	public UnInitializePropertyException() {
	}

	public UnInitializePropertyException(String message) {
		super(message);
	}

	public UnInitializePropertyException(String message, Throwable cause) {
		super(message, cause);
	}

	public UnInitializePropertyException(Throwable cause) {
		super(cause);
	}

}
